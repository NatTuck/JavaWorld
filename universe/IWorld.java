package universe;

import java.io.Serializable;

/** Represents a World in the Universe.  We assign each world a unique ID so
 *    messages can be matched and delivered. */
public interface IWorld extends Serializable{
    /** Get the registered name of this World (not necessarily unique) */
    public String name();
    /** Get this worlds unique ID */
    public long id();
    /** Is this IWorld the same as the given IWorld? */
    public boolean equals(IWorld w);
}

