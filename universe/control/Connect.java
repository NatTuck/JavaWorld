package universe.control;

/** Represents a Connect Message when a new World is added to the Universe */
public class Connect extends Message{
    private static final long serialVersionUID = 1;
    
    private String name;
    
    /** Create a Connect Message from a Name */
    public Connect(String n, long id){ super(id); this.name = n; }
    /** Is this a Connect Message? */
    public boolean isConnect(){ return true; }
    /** Get the sender's Name */
    public String name(){ return this.name; }
}

